    document.addEventListener("DOMContentLoaded", function() {
      const cssForm = document.getElementById("ch1_cssForm");
      const customStyle = document.getElementById("custom-style");

      cssForm.addEventListener("submit", function(event) {
      event.preventDefault();

      const flex = document.getElementById("ch1_flex").value;

    const css = `
      .ch1_coins {${flex};
}
    `;

    customStyle.innerHTML = css;
  });
});

